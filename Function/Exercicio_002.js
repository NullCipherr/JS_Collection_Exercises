//
// Concatenar Strings
//
// Crie uma função que concatene duas strings

function concatenarStrings(string1, string2)
{
    return string1 + " " + string2;
}

// Testes da Função
console.log(concatenarStrings("Olá", "Mundo")); // Olá Mundo
console.log(concatenarStrings("Bom", "Dia")); // Bom Dia
console.log(concatenarStrings("Boa", "Noite")); // Boa Noite
console.log(concatenarStrings("Boa", "Tarde")); // Boa Tarde
console.log(concatenarStrings("Seja", "Bem Vindo")); // Seja Bem Vindo