// Crie uma função que calcule a área de um retângulo

// Função que calcula a área de um retângulo
function areaRetangulo(base, altura)
{
    return base * altura ; // A fórmula da área do retângulo é base * altura
}

// Testes da Função
console.log(areaRetangulo(10, 5)); // 50
console.log(areaRetangulo(20, 10)); // 200
console.log(areaRetangulo(30, 15)); // 450
console.log(areaRetangulo(40, 20)); // 800