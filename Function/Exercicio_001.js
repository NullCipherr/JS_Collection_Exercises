// Função Quadrado
//
// Crie uma função que recebe um número e retorna o seu quadrado.

function quadrado(numero)
{
    return numero * numero ;
}

// Testes da Função Quadrado
console.log(quadrado(2)); // 4
console.log(quadrado(3)); // 9
console.log(quadrado(4)); // 16
console.log(quadrado(5)); // 25
